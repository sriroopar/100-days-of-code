def ceaser(text, shift ,direction):
    output_text= ''
    if direction == "decode":
            shift*= -1
    for letter in text :
        position = alphabet.index(letter)
        new_position = position + shift  
        output_text += alphabet[new_position]
    print(output_text)

to_be_continued = True
